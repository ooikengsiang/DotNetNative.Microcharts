namespace Microcharts
{
    /// <summary>
    /// Label display mode for the <seealso cref="T:Microcharts.DonutChart"/>
    /// </summary>
    public enum LabelMode
    {
        /// <summary>
        /// Not displaying label
        /// </summary>
        None,
        /// <summary>
        /// Display an equaly number of label in the Left and Right of the chart
        /// </summary>
        LeftAndRight,
        /// <summary>
        /// Display all the label at the Right of the chart
        /// </summary>
        RightOnly
    }
}
